#¿Cuántas vueltas dará un Fidget Spinner?

#Sabemos que un Fidget Spinner da 147 vueltas por minuto ¿Cuántas vueltas dará en 640 segundos?
# Para este ejercicio se desprecia el rozamiento con el aire.

#Análisis del problema:
#¿Qué me pide el problema? Rta:Calcular la cantidad de vueltas que dara el Spinner en 640 segundos
#¿Qué datos necesito? Cantidad de vueltas que se da por minuto y el tiempo en el cual quiero determinar las vueltas

#Algoritmo
minutos = 640/60
cantidad_de_vueltas = 147*minutos
print("La cantidad de vueltas que dara será:",cantidad_de_vueltas)
