#¿Cuánto costará el teléfono?


#Estamos interesados en comprar un nuevo teléfono móvil y en el escaparate de una tienda aparece que el móvil 
# cuesta 420€. El problema es que si nos esperamos a comprarlo al día siguiente sufrirá un incremento 
# porcentual del 20%. ¿Cuánto costará entonces el teléfono si nos esperamos?

#Análisis del problema:
#¿Qué me pide el problema? Rta: determinar el valor del teléfono si nos esperamos a que se haga el incremento del iva del 20%
#¿Qué datos necesito? valor del teléfono y el % de incremento (iva)

#Algoritmo
valor_sin_iva = 420
valor_con_iva = valor_sin_iva + valor_sin_iva*0.20

print("El valor con iva será de",valor_con_iva)
